<template>
  <form v-if="!isLogin">
    <div class="was-validated">
      <div>
        <input
          type="text"
          autocomplete="username"
          v-model="data.login"
          placeholder="Login"
        />
      </div>
      <div v-if="allAlerts.login">
        <p class="mt-2 mb-0 text-danger">{{ allAlerts.login }}</p>
      </div>
      <div>
        <input
          type="password"
          autocomplete="password"
          v-model="data.password"
          placeholder="Password"
        />
      </div>
      <div v-if="allAlerts.password">
        <p class="mt-2 mb-1 text-danger">{{ allAlerts.password }}</p>
      </div>
      <div v-if="allAlerts.error">
        <p class="mt-2 mb-1 text-danger">{{ allAlerts.error }}</p>
      </div>
      <div>
        <button type="button" class="btn btn-primary" @click="trySignIn">
          Sign In
        </button>
      </div>
    </div>
  </form>
</template>

<script>
import { mapGetters, mapActions } from 'vuex';

export default {
  components: {},
  mounted() {
    this.cleanErrors();
    console.log(
      'ðŸš€ ~ file: SignIn.vue ~ line 46 ~ trySignIn ~ this.lastNamemounted()',
      this.all,
      this.$route.name,
      this.lastName
    );
  },
  data() {
    return {
      data: {
        login: '',
        password: '',
      },
    };
  },
  computed: {
    ...mapGetters('router', ['lastName', 'all']),
    ...mapGetters('user', [
      'allAlerts',
      'isErrors',
      'isAlertsLogin',
      'isAlertsPassword',
      'isLogin',
    ]),
    isAuth() {
      return this.isAlertsLogin === true && this.isAlertsPassword === true
        ? false
        : true;
    },
  },
  methods: {
    ...mapActions('user', ['auth', 'cleanErrors']),
    async trySignIn() {
      let data = await this.auth({
        login: this.data.login,
        password: this.data.password,
        isAuth: this.isAuth,
      });
      if (data.access_token) {
        this.data.login = '';
        this.data.password = '';
        console.log(
          'ðŸš€ ~ file: SignIn.vue ~ line 81 ~ trySignIn ~ this.lastName',
          this.all,
          this.$route.name,
          this.lastName
        );
        this.$router.push({ name: this.lastName });
      }
    },
  },
  watch: {
    data: {
      handler() {
        this.auth(
          {
            login: this.data.login,
            password: this.data.password,
          },
          3000
        );
      },
      deep: true,
    },
  },
};
</script>
